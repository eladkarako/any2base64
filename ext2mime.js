function get_type(file){
  const EXTENSION = file.name.replace(/^.*\.([^\.]+)$/m, "$1").toLowerCase()
       ,TYPE      = TYPES[EXTENSION]
       ;
  return "string" === typeof TYPE ? TYPE : DEFAULT;
}


